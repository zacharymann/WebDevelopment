//  COMPARISON OPERATOR REVIEW
// Comparison operataors are used to test for true and false

// GREATER THAN
console.log(7 > 6);
console.log(2.1 > 1.234);

// LESS THAN
console.log(7 < 6)
console.log(0.9 < -4);;

// GREATER THAN OR EQUAL TO

console.log(10 >= 10);
console.log(3 >= -100);

// Less Than or Equal To
console.log(4 <= 3);
console.log(143 <= 176.04);

// Equal Comparisons
// Double Equals
// Equal value, but does not check type
console.log(3 == 2);
console.log("Chicken" == "Chicken");
console.log(3 == "3");

// Triple Equals (AKA Strict Equality)
// Equal value and equal type
console.log(7 === 7);
console.log('Friday' === `Friday`);
console.log("6" === 6);

// Not Equal Comparisons
// Not Equal
// NOT Equal value, but does not check type
console.log(3 != 3);
console.log(3 != 2);
console.log(3 != "3");

// Strict Not Equal
// Not equal value or not equal type
console.log(3 !== 3);
console.log(3 !== "3");